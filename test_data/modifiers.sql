-- Детальная проверка: все привязки с названиями продуктов
SELECT 
    m."Name" as "Модификатор",
    m."Price" as "Цена",
    p."Id" as "ProductId",
    p."Name" as "Продукт",
    c."Name" as "Категория"
FROM public."product_modifiers" pm
JOIN public."Modifiers" m ON pm."ModifiersId" = m."Id"
JOIN public."Products" p ON pm."ProductsId" = p."Id"
JOIN public."Categories" c ON p."Category_id" = c."Id"
ORDER BY m."Id", c."Id", p."Name";
